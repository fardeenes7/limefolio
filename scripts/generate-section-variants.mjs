import { readdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const root = process.cwd();
const sectionsDir = path.join(root, 'src', 'components', 'sections');
const registryPath = path.join(sectionsDir, '_renderer', 'VariantRegistry.tsx');
const checkOnly = process.argv.includes('--check');

const variantFilePattern = /^[a-z0-9][a-z0-9_-]*\.tsx$/;

async function pathExists(dir) {
    try {
        await readdir(dir);
        return true;
    } catch {
        return false;
    }
}

async function collectVariants() {
    const componentDirs = await readdir(sectionsDir, { withFileTypes: true });
    const variants = [];

    for (const componentDir of componentDirs) {
        if (!componentDir.isDirectory() || componentDir.name.startsWith('_')) continue;

        const componentPath = path.join(sectionsDir, componentDir.name);
        const files = await readdir(componentPath, { withFileTypes: true });

        for (const file of files) {
            if (!file.isFile() || !variantFilePattern.test(file.name)) continue;

            const variant = file.name.replace(/\.tsx$/, '');
            variants.push({ component: componentDir.name, variant });
        }
    }

    return variants.sort((a, b) => {
        const componentCompare = a.component.localeCompare(b.component);
        return componentCompare || a.variant.localeCompare(b.variant);
    });
}

function renderRegistry(variants) {
    const entries = variants
        .map(({ component, variant }) => (
            `    '${component}/${variant}': dynamic(() => import('../${component}/${variant}')),`
        ))
        .join('\n');

    return `"use client";

// AUTO-GENERATED by scripts/generate-section-variants.mjs
// Do not edit by hand. Add/remove variant files, then run npm run variants:generate.
import dynamic from 'next/dynamic';
import { ComponentType } from 'react';
import { SectionProps } from './SectionRenderer';

export const VariantRegistry: Record<string, ComponentType<SectionProps>> = {
${entries}
};
`;
}

if (!(await pathExists(sectionsDir))) {
    console.error(`Sections directory not found: ${sectionsDir}`);
    process.exit(1);
}

const variants = await collectVariants();
const nextContent = renderRegistry(variants);

if (checkOnly) {
    const currentContent = await readFile(registryPath, 'utf8');
    if (currentContent !== nextContent) {
        console.error('VariantRegistry.tsx is out of date. Run npm run variants:generate.');
        process.exit(1);
    }
    console.log(`VariantRegistry.tsx is up to date (${variants.length} variants).`);
} else {
    await writeFile(registryPath, nextContent);
    console.log(`Generated VariantRegistry.tsx with ${variants.length} variants.`);
}
